// Declarando uma variável
let nome;

// Atribuindo um valor à variável
nome = "Diego";
sobrenome = "Arruda";

// Imprimindo o valor das variáveis
console.log(nome + " " + sobrenome); // Imprime: Diego Arruda
console.log(sobrenome); // Imprime: Arruda

