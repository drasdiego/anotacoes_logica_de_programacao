// String - Cadeia de caracteres (mensagens de texto)
let texto1 = "Um texto qualquer";
console.log(texto1); // Imprime: Um texto qualquer

let texto2 = 'Um texto com aspas simples';
console.log(texto2); // Imprime: Um texto com aspas simples

let texto3 = "texto 'entre' aspas";
console.log(texto3); // Imprime: texto 'entre' aspas

// Template strings (permitem usar aspas simples e duplas diretamente)
let textoComTemplate = `Um 'texto' "qualquer"`;
console.log(textoComTemplate); // Imprime: Um 'texto' "qualquer"

// Exemplo de template string com interpolação de variável
let liquido = "café";
let textoExemplo = `Eu estou bebendo ${liquido}`;
console.log(textoExemplo); // Imprime: Eu estou bebendo café

// Números
let numeroInteiro = 1; // Inteiros
let numeroDecimal = 1.5;  // Decimais

console.log(numeroInteiro); // Imprime: 1
console.log(numeroDecimal);  // Imprime: 1.5

// Valores lógicos ou booleanos
// verdadeiro ou falso

let maiorDeIdade = false;
let saldoSuficiente = false; 

console.log(maiorDeIdade); // Imprime: false
console.log(saldoSuficiente); // Imprime: false

//Fim de exemplos