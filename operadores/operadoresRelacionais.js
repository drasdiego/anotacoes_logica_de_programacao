let numero1 = 5;  
let numero2 = 10;

console.log(5 > 10);   // false, porque 5 não é maior que 10
console.log(5 < 10);   // true, porque 5 é menor que 10

console.log(""); // Linha em branco

console.log(numero1 > numero2);  // false, 5 não é maior que 10
console.log(numero1 < numero2);  // true, 5 é menor que 10

console.log(""); // Linha em branco

console.log(5 == 10);  // false, 5 não é igual a 10
console.log(5 == 5);   // true, 5 é igual a 5
console.log(5 == "5"); // true, igualdade de valores, o JavaScript faz coerção de tipos
console.log(5 === 5);  // true, igualdade estrita, o valor e o tipo são iguais (número)

console.log(""); // Linha em branco

console.log(18 >= 18); // true, 18 é maior ou igual a 18
console.log(18 <= 18); // true, 18 é menor ou igual a 18 

// Exemplo de limitação de idade mínima 
console.log(5 <= 12); // true, 5 é menor ou igual a 12

console.log(""); // Linha em branco

// Análise de diferença de valores 
console.log(5 != 5);  // false, 5 não é diferente de 5
console.log(5 !== "5"); // true, 5 é diferente de "5" (tipo diferente (Numero x "Texto"))

